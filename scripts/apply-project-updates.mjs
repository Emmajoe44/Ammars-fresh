#!/usr/bin/env node
/**
 * Merge config/project-updates.json into config/brand.json and sync branding
 * to the web app, mobile app, and OpenAPI spec.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const brandPath = path.join(root, "config", "brand.json");
const artifactsPath = path.join(root, "config", "artifacts.json");
const defaultUpdatesPath = path.join(root, "config", "project-updates.json");

const artifacts = fs.existsSync(artifactsPath)
  ? JSON.parse(fs.readFileSync(artifactsPath, "utf8"))
  : { webDir: "AMMARS FRESH", mobileDir: "AMMARS FRESH-mobile" };

const args = process.argv.slice(2);
const fileArg = args.indexOf("--file");
const updatesPath =
  fileArg >= 0 && args[fileArg + 1]
    ? path.resolve(args[fileArg + 1])
    : defaultUpdatesPath;

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

function writeJson(file, data) {
  fs.writeFileSync(file, `${JSON.stringify(data, null, 2)}\n`, "utf8");
}

function mergeBrand(base, patch) {
  const merged = { ...base };
  const added = [];
  const changed = [];

  for (const [key, value] of Object.entries(patch)) {
    if (value === undefined || value === null || value === "") continue;
    if (!(key in base)) added.push(key);
    else if (JSON.stringify(base[key]) !== JSON.stringify(value)) changed.push(key);
    merged[key] = value;
  }

  return { merged, added, changed };
}

function escTs(s) {
  return String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function escDart(s) {
  return String(s).replace(/\\/g, "\\\\").replace(/'/g, "\\'");
}

function optionalTsField(key, value) {
  if (value === undefined || value === null || value === "") return "";
  return `  ${key}: "${escTs(value)}",\n`;
}

function generateBrandTs(brand) {
  const currencies = brand.currencies ?? ["SSP", "USD"];
  const defaultCurrency = brand.defaultCurrency ?? currencies[0] ?? "SSP";
  return `// AUTO-GENERATED by scripts/apply-project-updates.mjs — do not edit by hand.
export const brand = {
  name: "${escTs(brand.projectName)}",
  nameAr: "${escTs(brand.projectNameAr ?? brand.projectName)}",
  legalName: "${escTs(brand.legalName ?? brand.projectName)}",
  tagline: "${escTs(brand.tagline ?? "")}",
  taglineAr: "${escTs(brand.taglineAr ?? "")}",
  country: "${escTs(brand.country ?? "South Sudan")}",
${optionalTsField("city", brand.city)}${optionalTsField("mission", brand.mission)}${optionalTsField("missionAr", brand.missionAr)}${optionalTsField("vision", brand.vision)}  currencies: ${JSON.stringify(currencies)} as const,
  defaultCurrency: "${escTs(defaultCurrency)}" as const,
  supportEmail: "${escTs(brand.supportEmail ?? "")}",
  salesEmail: "${escTs(brand.salesEmail ?? brand.supportEmail ?? "")}",
  billingEmail: "${escTs(brand.billingEmail ?? brand.supportEmail ?? "")}",
  website: "${escTs(brand.website ?? "")}",
  metaDescription: "${escTs(brand.metaDescription ?? "")}",
} as const;

export type BrandCurrency = (typeof brand.currencies)[number];
`;
}

function generateBrandDart(brand) {
  const currencies = brand.currencies ?? ["SSP", "USD"];
  const defaultCurrency = brand.defaultCurrency ?? currencies[0] ?? "SSP";
  const currencyList = currencies.map((c) => `'${escDart(c)}'`).join(", ");
  const cityLine = brand.city ? `  static const String city = '${escDart(brand.city)}';\n` : "";
  const missionLine = brand.mission ? `  static const String mission = '${escDart(brand.mission)}';\n` : "";
  return `// AUTO-GENERATED by scripts/apply-project-updates.mjs — do not edit by hand.

class BrandConfig {
  BrandConfig._();

  static const String name = '${escDart(brand.projectName)}';
  static const String nameAr = '${escDart(brand.projectNameAr ?? brand.projectName)}';
  static const String legalName = '${escDart(brand.legalName ?? brand.projectName)}';
  static const String tagline = '${escDart(brand.tagline ?? "")}';
  static const String taglineAr = '${escDart(brand.taglineAr ?? "")}';
  static const String country = '${escDart(brand.country ?? "South Sudan")}';
${cityLine}${missionLine}  static const List<String> currencies = [${currencyList}];
  static const String defaultCurrency = '${escDart(defaultCurrency)}';
  static const String supportEmail = '${escDart(brand.supportEmail ?? "")}';
  static const String website = '${escDart(brand.website ?? "")}';
}
`;
}

function patchFile(filePath, replacers) {
  if (!fs.existsSync(filePath)) return false;
  let text = fs.readFileSync(filePath, "utf8");
  let touched = false;
  for (const [pattern, replacement] of replacers) {
    const next = text.replace(pattern, replacement);
    if (next !== text) {
      text = next;
      touched = true;
    }
  }
  if (touched) fs.writeFileSync(filePath, text, "utf8");
  return touched;
}

function patchCurrencyEnums(filePath) {
  if (!fs.existsSync(filePath)) return;
  let text = fs.readFileSync(filePath, "utf8");
  const next = text.replace(/enum: \[SSP, USD\]/g, "enum: [SSP, USD, USG]");
  if (next !== text) fs.writeFileSync(filePath, next, "utf8");
}

const baseBrand = fs.existsSync(brandPath) ? readJson(brandPath) : {};
let brand = { ...baseBrand };

if (fs.existsSync(updatesPath)) {
  const patch = readJson(updatesPath);
  const { merged, added, changed } = mergeBrand(baseBrand, patch);
  brand = merged;
  writeJson(brandPath, brand);

  console.log(`Applied updates from: ${updatesPath}`);
  if (added.length) console.log(`  New fields: ${added.join(", ")}`);
  if (changed.length) console.log(`  Updated fields: ${changed.join(", ")}`);
  if (!added.length && !changed.length) console.log("  No changes (file matches current brand.json).");
} else {
  writeJson(brandPath, brand);
  console.log(`No ${path.relative(root, updatesPath)} found — using config/brand.json as-is.`);
}

const webDir = path.join(root, "artifacts", artifacts.webDir);
const mobileDir = path.join(root, "artifacts", artifacts.mobileDir);
const webBrandTs = path.join(webDir, "src", "lib", "brand.ts");
const mobileBrandDart = path.join(mobileDir, "lib", "config", "brand_config.dart");

fs.mkdirSync(path.dirname(webBrandTs), { recursive: true });
fs.mkdirSync(path.dirname(mobileBrandDart), { recursive: true });
fs.writeFileSync(webBrandTs, generateBrandTs(brand), "utf8");
fs.writeFileSync(mobileBrandDart, generateBrandDart(brand), "utf8");

const openapiPath = path.join(root, "lib", "api-spec", "openapi.yaml");
const apiDescription = `${brand.legalName ?? brand.projectName} API`;
patchFile(openapiPath, [
  [/^  description: .+$/m, `  description: ${apiDescription}`],
  [/^  title: .+$/m, `  title: ${brand.projectName} API`],
]);
patchCurrencyEnums(openapiPath);

patchFile(path.join(mobileDir, "android", "app", "src", "main", "AndroidManifest.xml"), [
  [/android:label="[^"]*"/, `android:label="${brand.projectName}"`],
]);

const manifestPath = path.join(mobileDir, "web", "manifest.json");
if (fs.existsSync(manifestPath)) {
  const manifest = readJson(manifestPath);
  manifest.name = brand.projectName;
  manifest.short_name = brand.projectName;
  manifest.description = brand.metaDescription ?? brand.tagline ?? brand.projectName;
  writeJson(manifestPath, manifest);
}

patchFile(path.join(mobileDir, "pubspec.yaml"), [
  [
    /^description: .+$/m,
    `description: ${brand.projectName} mobile app for ${brand.country ?? "South Sudan"} farmers, retailers, and admins.`,
  ],
]);

console.log(`Synced brand to:`);
console.log(`  ${path.relative(root, webBrandTs)}`);
console.log(`  ${path.relative(root, mobileBrandDart)}`);
console.log(`  lib/api-spec/openapi.yaml`);
console.log(`Project: ${brand.projectName}`);
console.log(`Currencies: ${(brand.currencies ?? []).join(", ")}`);
console.log(`Web artifact: artifacts/${artifacts.webDir}`);
console.log(`Mobile artifact: artifacts/${artifacts.mobileDir}`);
